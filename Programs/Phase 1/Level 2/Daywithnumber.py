ch=int(input("Enter the number: "))

if ch == 1:
    print(F'Monday')
elif ch == 2:
    print(F'Tuesday')
elif ch == 3:
    print(F'Wednesday')
elif ch == 4:
    print(F'Thursday')
elif ch == 5:
    print(F'Friday')
elif ch == 6:
    print(F'Saturday')
elif ch == 7:
    print(F'Sunday')
else:
    print(F'No day')                            